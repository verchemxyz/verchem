export const getChemicalElementName = (symbol: string, language: string): string => {
  const element = chemicalElements[symbol];
  if (!element) return symbol;
  
  return element[language as keyof ChemicalTerm] || element.en;
};

export const getChemicalTerm = (term: string, language: string): string => {
  const chemicalTerm = chemicalTerms[term];
  if (!chemicalTerm) return term;
  
  return chemicalTerm[language as keyof ChemicalTerm] || chemicalTerm.en;
};