// Helper functions
export function getElementBySymbol(symbol: string): Element | undefined {
  return PERIODIC_TABLE.find(el => el.symbol === symbol)
}

export function getElementByAtomicNumber(atomicNumber: number): Element | undefined {
  return PERIODIC_TABLE.find(el => el.atomicNumber === atomicNumber)
}

export function getElementByName(name: string): Element | undefined {
  return PERIODIC_TABLE.find(el => el.name.toLowerCase() === name.toLowerCase())
}

export function getElementsByCategory(category: string): Element[] {
  return PERIODIC_TABLE.filter(el => el.category === category)
}

export function getElementsByPeriod(period: number): Element[] {
  return PERIODIC_TABLE.filter(el => el.period === period)
}

export function getElementsByGroup(group: number): Element[] {
  return PERIODIC_TABLE.filter(el => el.group === group)
}

export function searchElements(query: string): Element[] {
  const lowerQuery = query.toLowerCase()
  return PERIODIC_TABLE.filter(el =>
    el.name.toLowerCase().includes(lowerQuery) ||
    el.symbol.toLowerCase().includes(lowerQuery) ||
    el.atomicNumber.toString() === query
  )
}

// Get rare earth elements (Sc, Y, and lanthanides)
export function getRareEarthElements(): Element[] {
  return PERIODIC_TABLE.filter(el =>
    el.symbol === 'Sc' ||
    el.symbol === 'Y' ||
    el.category === 'lanthanide'
  )
}

// Get actinides
export function getActinides(): Element[] {
  return PERIODIC_TABLE.filter(el => el.category === 'actinide')
}

// Get superheavy elements (Z > 103)
export function getSuperheavyElements(): Element[] {
  return PERIODIC_TABLE.filter(el => el.atomicNumber > 103)
}

