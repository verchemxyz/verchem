// Helper functions
export function getCompoundByFormula(formula: string): Compound | undefined {
  return COMMON_COMPOUNDS.find(c => c.formula === formula || c.id === formula)
}

export function getCompoundByName(name: string): Compound | undefined {
  const lowerName = name.toLowerCase()
  return COMMON_COMPOUNDS.find(c =>
    c.name.toLowerCase() === lowerName ||
    c.iupacName?.toLowerCase() === lowerName ||
    c.thaiName?.toLowerCase() === lowerName
  )
}

export function searchCompounds(query: string): Compound[] {
  const lowerQuery = query.toLowerCase()
  return COMMON_COMPOUNDS.filter(c =>
    c.name.toLowerCase().includes(lowerQuery) ||
    c.formula.includes(query) ||
    c.iupacName?.toLowerCase().includes(lowerQuery) ||
    c.thaiName?.toLowerCase().includes(lowerQuery) ||
    c.uses?.some(use => use.toLowerCase().includes(lowerQuery))
  )
}

export function getCompoundsByHazard(hazardType: string): Compound[] {
  return COMMON_COMPOUNDS.filter(c =>
    c.hazards?.some(h => h.type === hazardType)
  )
}

export function getAcids(): Compound[] {
  return COMMON_COMPOUNDS.filter(c => c.pKa !== undefined && c.pKa < 7)
}

export function getBases(): Compound[] {
  return COMMON_COMPOUNDS.filter(c => c.pKb !== undefined)
}

